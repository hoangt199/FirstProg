    private void showLX() {
        modelLX.setRowCount(0);
        for (LaiXe laiXe : lsLX) {
            modelLX.addRow(new Object[]{
                laiXe.getMaLX(),
                laiXe.getHoten(),
                laiXe.getDiaChi(),
                laiXe.getTrinhdo()
            });
        }
    }

    private void showTuyen() {
        modelTuyen.setRowCount(0);
        for (Tuyen tuyen : lsTuyen) {
            modelTuyen.addRow(new Object[]{
                tuyen.getMaTuyen(),
                tuyen.getKhoangcach(),
                tuyen.getSodiemdung()
            });
        }
    }
