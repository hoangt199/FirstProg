    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int lid =tblLX.getSelectedRow();
        int tid = tblTuyen.getSelectedRow();
        if(tid == -1 || lid ==-1){
            JOptionPane.showMessageDialog(rootPane,"Vui long chon dong can them");
        } else{
            LaiXe l = lsLX.get(lid);
            Tuyen t = lsTuyen.get(tid);
            int soluot = Integer.parseInt(combo.getSelectedItem().toString());
            Pair<Tuyen,Integer> pair = new Pair(t, soluot);
            boolean status = home.addPC(l, pair);
            if(status == false){
                JOptionPane.showMessageDialog(rootPane,"So luot phai nho hon 15");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

